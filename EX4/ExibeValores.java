public class ExibeValores {
    public static void main(String[] args) {
      
        byte valorByte = 19;               // Ocupa 1 byte
        short valorShort = 1571;           // Ocupa 2 bytes
        int valorInt = 100000;             // Ocupa 4 bytes
        long valorLong = 2147483648L;      // Ocupa 8 bytes
        float valorFloat = 3.14f;          // Ocupa 4 bytes
        double valorDouble = 3.141592653589793; // Ocupa 8 bytes
        boolean valorBoolean = true;       // Ocupa 1 bit (dentro de 1 byte)
        char valorChar = 'K';              // Ocupa 2 bytes

        System.out.println("Valor byte: " + valorByte);
        System.out.println("Valor short: " + valorShort);
        System.out.println("Valor int: " + valorInt);
        System.out.println("Valor long: " + valorLong);
        System.out.println("Valor float: " + valorFloat);
        System.out.println("Valor double: " + valorDouble);
        System.out.println("Valor boolean: " + valorBoolean);
        System.out.println("Valor char: " + valorChar);
    }
}
