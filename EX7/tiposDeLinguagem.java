public class tiposDeLinguagem {
	
	public static void main (String[] args) {
		//Bom dia
		String tipoString = "Bom dia";
		
		//3
		int tipoInt = 3;
		long tipoLong = 3;
		float tipoFloat = 3;
		double tipoDouble = 3;
		byte tipoByte = 3;
		short tipoShort = 3;
		
		//235.13 
		double tipoDouble2 = 235.13;
		
		//true
		boolean tipoBoolean = true;
		
		//-135
		int tipoInt2 = -135;
		long tipoLong2 = -135;
		short tipoShort2 = -135;
		
		//256.23F
		float tipoFloat2 = 256.23F;
		
		//'A'
		char tipoChar = 'A';
		
		//6463275245745L
		long tipoLong3 = 6463275245745L;
		
		System.out.println("Tipo String: " + tipoString + "\n");
		
		System.out.println("Tipo int: " + tipoInt);
		System.out.println("Tipo Long: " + tipoLong);
		System.out.println("Tipo Float: " + tipoFloat);
		System.out.println("Tipo Double: " + tipoDouble);
		System.out.println("Tipo Byte: " + tipoByte);
		System.out.println("Tipo Short: " + tipoShort + "\n");
		
		System.out.println("Tipo Double: " + tipoDouble2 + "\n");
		
		System.out.println("Tipo Boolean: " + tipoBoolean + "\n");
		
		System.out.println("Tipo int: " + tipoInt2);
		System.out.println("Tipo Long: " + tipoLong2);
		System.out.println("Tipo Short: " + tipoShort2 + "\n");
		
		System.out.println("Tipo Float: " + tipoFloat2 + "\n");
		
		System.out.println("Tipo Char: " + tipoChar + "\n");
		
		System.out.println("Tipo Long: " + tipoLong3);
	}
}
